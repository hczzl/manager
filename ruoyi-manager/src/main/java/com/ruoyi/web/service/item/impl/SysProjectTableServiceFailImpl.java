package com.ruoyi.web.service.item.impl;


import com.ruoyi.web.domain.SysProjectTableFail;
import com.ruoyi.web.service.item.SysProjectTableFailService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

@Service
public class SysProjectTableServiceFailImpl implements SysProjectTableFailService {

    @Autowired
    private SysProjectTableFailService sysProjectTableFailService;

    @Override
    public int insertFailProject(SysProjectTableFail sysProjectTableFail) {
        return sysProjectTableFailService.insertFailProject(sysProjectTableFail);
    }

    @Override
    public int selectId(SysProjectTableFail sysProjectTableFail) {
        return sysProjectTableFailService.selectId(sysProjectTableFail);
    }
}

